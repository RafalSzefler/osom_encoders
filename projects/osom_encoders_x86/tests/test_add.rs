#![allow(non_snake_case)]
mod common;

use rstest::rstest;

use osom_encoders_x86::encoding::*;
use osom_encoders_x86::models::*;

#[rstest]
#[case(-2, &[0x04, 0xFE])]
#[case(-1, &[0x04, 0xFF])]
#[case(0, &[0x04, 0x00])]
#[case(1, &[0x04, 0x01])]
#[case(10, &[0x04, 0x0A])]
#[case(127, &[0x04, 0x7F])]
fn test_add_AL_imm8(#[case] imm8: i8, #[case] expected: &[u8]) {
    let imm8 = Immediate8::from_i8(imm8);
    let instr = universal::encode_add_AL_imm8(imm8);
    common::assert_encoded_instruction_eq(expected, &instr);
}

